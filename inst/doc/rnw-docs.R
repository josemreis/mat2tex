## ------------------------------------------------------------------------
library(mat2tex)

## ----echo=FALSE----------------------------------------------------------
# reset default values. build_vignettes() seems to use current environment
mat2tex:::mat2tex_options_init()

## ------------------------------------------------------------------------
set.seed(1)
A <- matrix(runif(4), 2)

## ----echo=TRUE, results='asis'-------------------------------------------
"$$" %_% xm(A) %_% "$$"

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A)

## ------------------------------------------------------------------------
mat2tex_options(mathenvir=3)

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A)

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A, label="mylabel")

## ------------------------------------------------------------------------
mat2tex_options(mtype="bmatrix", digits=3)

## ----results='asis'------------------------------------------------------
d <- svd(A)
xx("A = UDV^T =", d$u, diag(d$d), xmt(d$v))

## ----results='asis'------------------------------------------------------
splt <- xx("AA^T & = ", A, t(A), "\\\\",
           "& =", A %*% t(A), e="split")
xx(splt, e="equation", label="eq1")

## ----results='asis'------------------------------------------------------
xx("AA^T  = ", A, t(A), "\\\\", 
   " =", A %*% t(A), e=9, label="eq2")

## ----results='asis'------------------------------------------------------
xx("AA^T  &= ", A, t(A), "\\\\", 
   "&=", A %*% t(A), e=5, label="eq3")

## ----results='asis'------------------------------------------------------
xx("AA^T  = ", A, t(A), "\\\\", 
   "=", A %*% t(A), e=7, label="eq3")

