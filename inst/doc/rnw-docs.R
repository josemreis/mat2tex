## ------------------------------------------------------------------------
library(mat2tex)

## ------------------------------------------------------------------------
set.seed(1)
A <- matrix(runif(4), 2)

## ----echo=TRUE, results='asis'-------------------------------------------
"$$" %_% xm(A) %_% "$$"

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A)

## ------------------------------------------------------------------------
mat2tex_options(mathenvir=3)

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A)

## ----echo=TRUE, results='asis'-------------------------------------------
xx(A, label="mylabel")

## ------------------------------------------------------------------------
mat2tex_options(mtype="bmatrix", digits=3)

## ----results='asis'------------------------------------------------------
d <- svd(A)
xx("A = UDV^T =", d$u, diag(d$d), xmt(d$v))

